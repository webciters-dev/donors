// Helper functions
export const getCurrencyFromCountry = (country) => {
  const countryData = UNIVERSITIES_BY_COUNTRY[country];
  return countryData ? countryData.currency : 'USD';
};

export const getCurrencySymbolFromCountry = (country) => {
  const countryData = UNIVERSITIES_BY_COUNTRY[country];
  return countryData ? countryData.symbol : '$';
};

export const getUniversitiesForCountry = (country) => {
  const countryData = UNIVERSITIES_BY_COUNTRY[country];
  return countryData ? countryData.universities : [];
};

export const getSupportedCountries = () => {
  return Object.keys(UNIVERSITIES_BY_COUNTRY);
};

export const validateUniversityCountry = (university, country) => {
  const universities = getUniversitiesForCountry(country);
  return universities.includes(university) || universities.includes('Other');
};

// Legacy exports for backward compatibility
export const getUniversities = () => {
  return Object.values(UNIVERSITIES_BY_COUNTRY).flatMap(country => 
    country.universities.filter(uni => uni !== 'Other').map(uni => ({
      name: uni,
      country: Object.keys(UNIVERSITIES_BY_COUNTRY).find(key => 
        UNIVERSITIES_BY_COUNTRY[key].universities.includes(uni)
      )
    }))
  );
};

export const searchUniversities = (query) => {
  const searchTerm = query.toLowerCase();
  return getUniversities().filter(university => 
    university.name.toLowerCase().includes(searchTerm) ||
    university.country.toLowerCase().includes(searchTerm)
  );
};